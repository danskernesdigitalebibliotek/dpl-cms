describe("Wiremock", () => {
  it("returns stubbed responses", () => {
    cy.createMapping({
      request: {
        method: "GET",
        urlPath: "/smoke/test",
      },
      response: {
        headers: {
          "Content-Type": "application/json",
        },
        jsonBody: {
          success: true,
        },
      },
    });
    cy.request("http://dummy/smoke/test")
      .its("body")
      .should("deep.equal", { success: true });
  });

  beforeEach(() => {
    cy.resetMappings();
  });

  afterEach(() => {
    cy.logMappingRequests();
  });
});
